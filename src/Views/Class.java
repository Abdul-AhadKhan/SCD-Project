    private void Option4PanelMouseClicked(MouseEvent evt) throws SQLException {

        int result = JOptionPane.showConfirmDialog(null, "Do You Want To Leave Classroom", "Conformation", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE );
        if (result == JOptionPane.YES_OPTION){
            if (studentController.leaveClassroom(cName)){
                JOptionPane.showConfirmDialog(null, "You Left Classroom", "Info", JOptionPane.OK_CANCEL_OPTION, JOptionPane.INFORMATION_MESSAGE);
                dispose();
            }
        }
    }

